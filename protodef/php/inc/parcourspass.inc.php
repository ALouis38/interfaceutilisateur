					<h3>Parcours de Soutien en langues... 4 étapes chronologiques</h3>
						<p>
								Ce parcours en langues est structuré en 4 étapes (2 durant le premier semestre et 2 durant le second semestre).
							A chaque Etape correspond un livret de liaison à utiliser sur cinq semaines :
							<ul>
								<li>des activités en ligne pendant les quatre premières semaines de chaque Etape</li>
								<Li>une production écrite lors de la semaine 5 de chaque Etape</li>
							</ul>
							La fin de chaque étape se marque par le dépôt (obligatoire) d'un ou de plusieurs fichiers dans un collecticiel (espace d'échange de documents).
							A l'issue de :
							<ul>
								<li>l'étape 1, vous déposerez la version finale de votre "Livret de liaison 1"</li>
								<li>l'étape 2, vous déposerez la version finale de votre "Livret de liaison 2" ainsi que les documents qui correspondent aux tâches de fin de semestre. Le tuteur évaluera ces dernières ainsi que votre participation durant ce premier semestre (évaluation des livrets de liaison 1 et 2)</li>
								<li>l'étape 3, vous déposerez la version finale de votre "Livret de liaison 3"</li>
								<li>l'étape 4, vous déposerez la version finale de votre "Livret de liaison 4" ainsi que les documents qui correspondent aux tâches de fin de semestre. Le tuteur évaluera ces dernières ainsi que votre participation durant ce second semestre (évaluation des livrets de liaison 3 et 4)</li>
							</ul>
						</p>
