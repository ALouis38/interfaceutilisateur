						<h4>Petit lexique à l'attention de celui ou celle qui veut tout savoir ;-)</h4>

						<h5> Mais que représente donc le "livret de liaison" dont on parle ?</h5>
						<p>
						Le livret de liaison assure la relation de travail entre vous et votre tuteur. C'est un fichier Word que vous téléchargerez dans certaines parties (les "collecticiels") de ce parcours et dans lequel vous pourrez :
							<ul>
								<li>trouver la description de ce qu'il s'agit de réaliser durant l'étape dans laquelle vous travaillez (ici, l'étape 1)</li>
								<li>laisser un commentaire à l'attention de votre tuteur</li>
								<li>enregistrer sur votre ordinateur pour ensuite le déposer sur la plateforme et enfin, recevoir, en retour, les commentaires de votre tuteur indiqués dans ce même livret. </li>
							</ul>
						</p>
						<h5>Et un "collecticiel" alors ?</h5>
						<p>
							Un collecticiel est un espace de partage de fichiers. Vous pourrez télécharger les fichiers disponibles et en déposer d'autres. 
							Dans l'organisation que nous avons prévue, c'est là que vous récupérerez les livrets de liaison (en cliquant sur leur nom) et 
							c'est là aussi que vous et votre tuteur déposerez les livrets (en cliquant sur un lien intitulé "Déposer"). A noter que, 
							pour une même étape, vous vous échangerez différentes versions d'un même livret... versions actualisées au fur et à mesure de 
							la progression de votre travail dans l'étape.
						</p>


